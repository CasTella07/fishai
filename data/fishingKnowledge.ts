/**
 * ユーザーメッセージから関連釣法を返す
 */
export function findRelevantMethods(userMessage: string): FishingMethod[] {
  const lower = userMessage;
  const matched = new Set<string>();

  for (const [id, keywords] of Object.entries(FISHING_KEYWORDS)) {
    if (keywords.some((kw) => lower.includes(kw))) {
      matched.add(id);
    }
  }

  return FISHING_KNOWLEDGE.filter((m) => matched.has(m.id));
}

